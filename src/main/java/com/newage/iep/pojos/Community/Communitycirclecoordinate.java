public class Communitycirclecoordinate {
    private String id;
    private String communityCircleLng;
    private String communityCircleLat;
    private String communityId;
    private String communityCircleRadius;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCommunityCircleLng() {
        return communityCircleLng;
    }

    public void setCommunityCircleLng(String communityCircleLng) {
        this.communityCircleLng = communityCircleLng;
    }

    public String getCommunityCircleLat() {
        return communityCircleLat;
    }

    public void setCommunityCircleLat(String communityCircleLat) {
        this.communityCircleLat = communityCircleLat;
    }

    public String getCommunityId() {
        return communityId;
    }

    public void setCommunityId(String communityId) {
        this.communityId = communityId;
    }

    public String getCommunityCircleRadius() {
        return communityCircleRadius;
    }

    public void setCommunityCircleRadius(String communityCircleRadius) {
        this.communityCircleRadius = communityCircleRadius;
    }
}
