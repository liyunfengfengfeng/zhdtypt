public class Circlecoordinate {
    private String id;
    private String circleLng;
    private String circleLat;
    private String enclosureId;
    private String circleRadius;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCircleLng() {
        return circleLng;
    }

    public void setCircleLng(String circleLng) {
        this.circleLng = circleLng;
    }

    public String getCircleLat() {
        return circleLat;
    }

    public void setCircleLat(String circleLat) {
        this.circleLat = circleLat;
    }

    public String getEnclosureId() {
        return enclosureId;
    }

    public void setEnclosureId(String enclosureId) {
        this.enclosureId = enclosureId;
    }

    public String getCircleRadius() {
        return circleRadius;
    }

    public void setCircleRadius(String circleRadius) {
        this.circleRadius = circleRadius;
    }
}
